#include<iostream>
#include<conio.h>
using namespace std;

int main()
{
	int no;
	
	
	  cout<<"Enter the NO: ";
	  cin>>no;
	  
      	
	   if (no == 0)
	   {
	   	cout<<"NO is ZERO"<<endl;
	   }
	   if(no % 2 == 0)
	   {
	   	cout<<"No is EVEN"<<endl;
	   }
	   else
	   {
	   	cout<<"No is ODD"<<endl;
	   }
	
}
